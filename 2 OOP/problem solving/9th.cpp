/*  C++ Program to find Addition of Two Numbers  */

#include<iostream>

using namespace std;

int main()
{
    int x,y,sum;

    cout<<"Enter first number :: ";
    cin>>x;
    cout<<"\nEnter second number :: ";
    cin>>y;

    sum=x+y;

    cout<<"\nSum of two numbers [ "<<x<<" + "<<y<<" ] = "<<sum<<"\n";

    return 0;
}